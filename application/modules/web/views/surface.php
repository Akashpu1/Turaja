<h4>1. GOTA</h4>
<p>Gota embellishment was sought after by people of all communities and social strata, who desired it in some form of clothing or accessory that they possessed.</p>
<p>Gota is a strip of gold or silver ribbon of varying width, woven in plain, satin or twill weave. While the gold ribbons are made with yellow silk or cotton and gold yarn, the silver ones are made with white silk or cotton and silver yarn. Embroidery using gota is known as gota-patti work or lappe-ka-kaam.</p>
<p>This form of fabric ornamentation was perhaps developed in Rajasthan, where it has been practised since medieval times. It is also popular in Punjab, Haryana and Andhra Pradesh. Early examples from the state of Jammu & Kashmir exist as well.</p>
<h4>2. MUKAISH</h4>
<p>Mukaish or Muqaish or Mukesh or Mokesh work is a form of embellishment work in which strips of metallic wire are inserted into the fabric and then twisted to create metallic embroidery. This type of embroidery involves twisting thin metallic threads to create patterns all over the fabric. Also referred to as ‘sachcha kaam’, real silver and gold were traditionally used for this work. However, a variety of threads and metals are now being used. This form of embroidery can be done on all kinds of clothing items, right from sarees, lehengas and salwar kameez to shirts, tunics, kurtis and more.</p>
<h4>3. MIRROR WORK</h4>
<p>Sheesha or mirror embroidery is traced back to 17th century in Iran and it is said to have been brought to Indian through various travelers during the Mughal era. The mirrors are affixed on to the fabric by special cross stitch embroidery that encloses the mirror, and provides it a casing.</p>
<h4>4. SEQUINS</h4>
<h4>5. ZARI/ DABKA</h4>
<p>Fine metal thread called zari is woven into or embroidered onto many fabrics from India. When the wire is wrapped or woven to form intricate textures and shapes, it is called zardosi or dabka. Zardosi often includes crystals, sequins, and pearl beading.  High-quality zardosi is so finely and evenly worked that it's hard to see the individual coils. On very light fabrics, the zari thread is satin-stitched and pressed flat in a style called kamdani.</p>

